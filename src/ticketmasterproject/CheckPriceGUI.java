    private void totalPriceButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_totalPriceButtonActionPerformed
        // TODO add your handling code here:           
        
        //check if the text field is empty
        if(numTicketsTextField.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Please enter the number of tickets!"); 
            numTicketsTextField.requestFocus();            
        }
        
        //ckeck if the user enter the number of tickets as 0
        else if (Integer.parseInt(numTicketsTextField.getText())<=0){
            JOptionPane.showMessageDialog(null, "You entered a wrong number of tickets, The number of tickets can not be 0!!");
            numTicketsTextField.requestFocus();
        }     
        else{    
            
            //calculate the total cost for Youth
            if(youthCheckBox.isSelected()){
                int numberTickets = Integer.parseInt(numTicketsTextField.getText());
                ((Youth)(p)).setNumberTickets(numberTickets);                
                ((Youth)(p)).computeTotalCost();
                double totalCost=((Youth)(p)).getTotalCost();
                totalCostLabel.setText("" +totalCost);
            }    
            
            //calculate the total cost for Adult
            else if(adultCheckBox.isSelected()){
                int numberTickets = Integer.parseInt(numTicketsTextField.getText());
                ((Adult)(p)).setNumberTickets(numberTickets);
                ((Adult)(p)).computeTotalCost();
                double totalCost=((Adult)(p)).getTotalCost();
                totalCostLabel.setText("" +totalCost);
            }
            
            //calculate the total cost for Elder
            else if(elderCheckBox.isSelected()){
                int numberTickets = Integer.parseInt(numTicketsTextField.getText());
                ((Elder)(p)).setNumberTickets(numberTickets);
                ((Elder)(p)).computeTotalCost();
                double totalCost=((Elder)(p)).getTotalCost();
                totalCostLabel.setText("" +totalCost);
            }            
            else{                
                JOptionPane.showMessageDialog(null, "Please select your age");                
            }                      
        }             
    }//GEN-LAST:event_totalPriceButtonActionPerformed
  
