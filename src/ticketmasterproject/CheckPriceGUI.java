    private void numTicketsTextFieldKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_numTicketsTextFieldKeyPressed
        // TODO add your handling code here:
        
        //check if the user enterd  numbers only
        int key = evt.getKeyCode();
            if((key>=KeyEvent.VK_0 && key<=KeyEvent.VK_9)||(key>=KeyEvent.VK_NUMPAD0&&key<=KeyEvent.VK_NUMPAD9)||key==KeyEvent.VK_BACK_SPACE){
                //numTicketsTextField.setEditable(true);
                //numTicketsTextField.setBackground(Color.white);
            }
            else{
                
                //show message box                 
                numTicketsTextField.setBackground(Color.red);
                JOptionPane.showMessageDialog(null, "Enter numbers only, letters or any special characters are not allowed"); 
                numTicketsTextField.setEditable(true);
                numTicketsTextField.setBackground(Color.white);
                numTicketsTextField.setText("");
                numTicketsTextField.requestFocus();
            }
    }//GEN-LAST:event_numTicketsTextFieldKeyPressed

