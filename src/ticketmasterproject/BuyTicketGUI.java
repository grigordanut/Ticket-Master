    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        // TODO add your handling code here:
        
        //check if the text fields are empty
        if (firstNameTextField.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Please enter your: First Name ");
            firstNameTextField.requestFocus();
        }
        else if (sureNameTextField.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Please enter your: Sure Name ");
            sureNameTextField.requestFocus();
        } 
        
        else if (emailTextField.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Please enter your: email ");
            emailTextField.requestFocus();
        }
        
        else if (!(Pattern.matches("^[a-zA-Z0-9]+[@]{1}+[a-zA-Z0-9]+[.]{1}+[a-zA-Z0-9]+$",emailTextField.getText()))){
            JOptionPane.showMessageDialog(null, "Please enter a valid email","Error",JOptionPane.ERROR_MESSAGE);
            emailTextField.requestFocus();
        }
        
        else if(phoneTextField.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Please enter your: phone ");
            phoneTextField.requestFocus();
        }   
        
        else if(buttonGroup1.getSelection()==null){
            JOptionPane.showMessageDialog(null, "Please select your: gender ");
        }     
        
        else if(categoryLabel.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Please press Buy New Ticket, to buy another ticket.");         
        }
        
        else{   
            
            //grab text from fields           
            if (LoginGUI.chekp.getYouthSelected()){
               p = new Youth(); 
            }
            else if(LoginGUI.chekp.getAdultSelected()){
                p = new Adult();
            }
            else if (LoginGUI.chekp.getElderSelected()){
                p = new Elder();
            }
            String firstName = firstNameTextField.getText();
            String sureName = sureNameTextField.getText();
            String email = emailTextField.getText();
            String phone = phoneTextField.getText();
            String gender="";
                   
            if(maleRadioButton.isSelected()){
                gender = "Male";
            }
            else if (femaleRadioButton.isSelected()){
                gender= "Female";
            }            
            String seatCategory = categoryLabel.getText();             
            int numberTickets=Integer.parseInt(numTicketsLabel.getText());        
            double totalCost = Double.parseDouble(totalPriceLabel.getText());          

            //add data to object
            p.setFirstName(firstName);
            p.setSureName(sureName);
            p.setEmail(email);
            p.setPhone(phone);
            p.setGender(gender);
            p.setSeatCategory(seatCategory);
            p.setNumberTickets(numberTickets);              

            if (p instanceof Youth){
                ((Youth)(p)).setTotalCost(totalCost);
            }
            else if (p instanceof Adult){
                ((Adult)(p)).setTotalCost(totalCost);           
            }
            else if (p instanceof Elder){
                ((Elder)(p)).setTotalCost(totalCost);
            } 
            
            JOptionPane.showMessageDialog(null,"Your details has been saved!!");
            
            //add object to array
            ticketList.add(p);
        
            //write data to the file
            writeTicketToFile();
            
            //empty all text fields 
            firstNameTextField.setText("");
            sureNameTextField.setText("");
            emailTextField.setText("");
            phoneTextField.setText("");
            buttonGroup1.clearSelection();
            categoryLabel.setText("");
            ageLabel.setText("");  
            ticketPriceLabel.setText("");
            numTicketsLabel.setText("");
            totalPriceLabel.setText("");   
        }         
    }//GEN-LAST:event_saveButtonActionPerformed

    private void writeTicketToFile(){
        
        //data members and new objects
        try{
            File f = new File("tickets.txt");
            FileWriter fw = new FileWriter(f,true);        
            BufferedWriter bw = new BufferedWriter(fw);
            ArrayList<String> listNames = new ArrayList();
            for (int i=0; i<listNames.size(); i++ ){                
                bw.newLine();
            }
            
            //grab text from fields
            String firstName = firstNameTextField.getText();
            String sureName = sureNameTextField.getText();  
            String numTickets = numTicketsLabel.getText();
            
            //write data to a file
            bw.write(firstName); 
            bw.newLine();
            bw.write(sureName); 
            bw.newLine();
            bw.write(numTickets); 
            bw.newLine();
            bw.close();
            JOptionPane.showMessageDialog(null, "Thanks "+firstName+" "+sureName+" for buying "+numTickets+" tickets");
            
        //check if data can be write to a file
        }catch(IOException e){
            System.out.println(e);
            System.out.println("Sorry, problem writing to a file");
        }
    }
    
