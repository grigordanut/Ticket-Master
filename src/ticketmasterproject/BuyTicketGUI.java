    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        // TODO add your handling code here:
        
        //declare an object search
        Person search;
        
        //set boolean
        boolean found=false;
        
        //check if the data match
        if (firstNameTextField.getText().equals("") && sureNameTextField.getText().equals("")){
            JOptionPane.showMessageDialog(null,"You did not enter a first or sure name");
            firstNameTextField.requestFocus();
        }
        else{
            //loop through ticketList
            for (int i=0;i<ticketList.size();i++){
                search=ticketList.get(i);
                if ((search.getFirstName().equalsIgnoreCase(firstNameTextField.getText())) || (search.getSureName().equalsIgnoreCase(sureNameTextField.getText()))){
                    found=true;
                    JOptionPane.showMessageDialog(null,search.printDetails());
                    break;                   
                }
            }
            
            //check the boolean           
            if (!found){
                JOptionPane.showMessageDialog(null,"Not found!"); 
            }
        }
    }//GEN-LAST:event_searchButtonActionPerformed

    private void printListButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_printListButtonActionPerformed
        // TODO add your handling code here:
        try{
            File f = new File("tickets.txt");
            FileReader fr = new FileReader(f);
            BufferedReader br = new BufferedReader(fr);
            String firstName;
            String sureName;
            String numTickets;        
            
            firstName = br.readLine();
            sureName = br.readLine();
            numTickets=br.readLine();
            
            ArrayList<String> listNames = new ArrayList();
            while(firstName!=null){    
                listNames.add(firstName);   
                listNames.add(sureName);
                listNames.add(numTickets);
                
                firstName = br.readLine();
                sureName = br.readLine();
                numTickets=br.readLine();
            }  
            String s="";
            for (int i=0;i<listNames.size();i=i+3){
                
                s=s+listNames.get(i)+" "+listNames.get(i+1)+" has bought " + listNames.get(i+2) + " tickets\n";                
            }
            JOptionPane.showMessageDialog(null, s);
            br.close();             
        }catch (IOException e){
            System.out.println(e);
            System.out.println("Sorry I cannot read the file");
        }
    }//GEN-LAST:event_printListButtonActionPerformed

