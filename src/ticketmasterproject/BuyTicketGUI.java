    private void phoneTextFieldKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_phoneTextFieldKeyPressed
        // TODO add your handling code here:
        int key = evt.getKeyCode();
            if((key>=KeyEvent.VK_0 && key<=KeyEvent.VK_9)||(key>=KeyEvent.VK_NUMPAD0&&key<=KeyEvent.VK_NUMPAD9)||key==KeyEvent.VK_BACK_SPACE){
                phoneTextField.setEditable(true);
                phoneTextField.setBackground(Color.white);
            }
            else{
                phoneTextField.setEditable(false);
                phoneTextField.setBackground(Color.red);
                JOptionPane.showMessageDialog(null, "Enter only numbers, letters or any special characters are not allowed");                
                phoneTextField.requestFocus();
            }        
    }//GEN-LAST:event_phoneTextFieldKeyPressed

